public class Word {
    
    private String word;
    private String connector;
    
    public String getWord() {
    
        return word;
    }
    
    public void setWord( String word ) {
    
        this.word = word;
    }
    
    public String getConnector() {
    
        return connector;
    }
    
    public void setConnector( String connector ) {
    
        this.connector = connector;
    }
    
}
